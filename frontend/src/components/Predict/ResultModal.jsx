import React from "react";
import {
  FiX,
  FiAlertTriangle,
  FiCheckCircle,
  FiPercent,
} from "react-icons/fi";

export default function ResultModal({ result, onClose }) {
  if (!result) return null;

  const highRisk = result.prediction === 1;

  const confidence = Math.round(
    (
      highRisk
        ? result.probability_disease
        : result.probability_no_disease
    ) * 100
  );

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/40 backdrop-blur-sm p-5">
      <div className="w-full max-w-lg rounded-2xl bg-white shadow-xl">

        {/* Header */}
        <div className="flex items-center justify-between border-b p-6">
          <div className="flex items-center gap-3">

            {highRisk ? (
              <FiAlertTriangle className="text-3xl text-red-600" />
            ) : (
              <FiCheckCircle className="text-3xl text-green-600" />
            )}

            <div>
              <h2 className="font-poppins text-2xl font-semibold text-gray-900">
                Prediction Result
              </h2>

              <p className="font-nunito text-sm text-gray-500">
                AI-generated heart disease assessment
              </p>
            </div>
          </div>

          <button
            onClick={onClose}
            className="rounded-lg p-2 hover:bg-gray-100"
          >
            <FiX size={20} />
          </button>
        </div>

        {/* Body */}
        <div className="space-y-6 p-6">

          {/* Risk */}
          <div
            className={`rounded-xl border p-5 ${
              highRisk
                ? "border-red-200 bg-red-50"
                : "border-green-200 bg-green-50"
            }`}
          >
            <p className="text-sm text-gray-500 font-nunito">
              Prediction
            </p>

            <h3
              className={`mt-1 text-2xl font-bold font-poppins ${
                highRisk
                  ? "text-red-600"
                  : "text-green-600"
              }`}
            >
              {highRisk
                ? "High Risk Detected"
                : "Low Risk Detected"}
            </h3>
          </div>

          {/* Confidence */}
          <div className="rounded-xl border p-5">

            <div className="flex items-center gap-2 mb-3">
              <FiPercent className="text-red-600" />
              <span className="font-semibold font-poppins">
                Confidence
              </span>
            </div>

            <div className="w-full h-3 rounded-full bg-gray-200 overflow-hidden">

              <div
                className={`h-full ${
                  highRisk
                    ? "bg-red-600"
                    : "bg-green-600"
                }`}
                style={{
                  width: `${confidence}%`,
                }}
              />
            </div>

            <p className="mt-2 font-roboto text-lg font-bold">
              {confidence}%
            </p>

          </div>

          {/* Recommendation */}
          <div className="rounded-xl border p-5">

            <h4 className="font-poppins font-semibold mb-2">
              Recommendation
            </h4>

            <p className="font-nunito text-gray-600 leading-relaxed">

              {highRisk
                ? "The model indicates an increased likelihood of heart disease. Please consult a qualified cardiologist for further evaluation and appropriate medical advice."
                : "The model indicates a lower likelihood of heart disease. Continue maintaining a healthy lifestyle with regular exercise, balanced nutrition, and periodic health check-ups."}

            </p>

          </div>

          {/* Disclaimer */}
          <div className="text-center text-xs text-gray-400 font-nunito">
            This prediction is generated by an AI model and should not
            be considered a medical diagnosis.
          </div>

        </div>

      </div>
    </div>
  );
}